package Loop;

public class Do_while {
	
	// Program to find the sum of natural numbers from 1 to 100.
	
	 
		 public static void main(String[] args) {

			    int i = 1, n = 10;

			    // do...while loop from 1 to 5
			    do 
			    {
			      System.out.println(i);
			      i++;
			    } while(i <= n);
			  }
}
