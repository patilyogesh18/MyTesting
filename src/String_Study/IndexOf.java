package String_Study;

public class IndexOf {
	public static void main(String[] args) 
	{
		String s1="Chetan";
		String s2="Dandekar";
		String s3="Yogita";
		String s4="Rajkumar";
		System.out.println(s4.indexOf('a'));
		
		System.out.println(s2.lastIndexOf('a'));
		
		System.out.println(s1.replace("Chetan","yogita"));
		System.out.println(s1.replace('n', 'k'));
		System.out.println(s1.replace("tan", "tak"));
		System.out.println(s3.replace("ita", "esh"));
	}


}
