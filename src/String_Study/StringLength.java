package String_Study;

public class StringLength {
	

//		length() method is used to find the number of character in the String
//		syntax:--> objname.length();
		
		public static void main(String[] args) 
		{
			//String name="I_LOVE_MY_INDIA"
			
			
			
			String s1="Velocity";
			String s2="Chetan Dandekar";
			
			System.out.println("The length of the String is "+s1.length());
			int Length=s1.length();     //Length is a reference variable or temporary variable
			System.out.println("Length of the string is "+Length);
			
			int LenthofString=s2.length(); //LengthofString is a reference variable or temporary variable
			System.out.println("Length of the string is "+LenthofString);
		}


}
